
package Practice;

public class EscapeSequence {
    public static void main(String args[]){
        
        System.out.println("Back \b space\n"); //1 space reduce
        System.out.println("This is \t Tab\n");  //4 space increase
        System.out.println("New\nLine\n"); 
        System.out.println("Carriage \r return\n");  //output after \r words as --> return
        System.out.println("Double \"Quote\"\n");   // "Quote"
        System.out.println("Single \'Quote\'\n");    //'Quote'
        System.out.println("Back \\ Slash\n");     //  Back \ Slash
    }
}
